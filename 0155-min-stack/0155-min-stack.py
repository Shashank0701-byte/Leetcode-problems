class MinStack:

    def __init__(self):
        self.stack = []

    def push(self, val: int) -> None:
        if not self.stack:
            self.stack.append((val,val))
        else:
            curr_min = self.stack[-1][1]
            new_min = min(val, curr_min)

            self.stack.append((val, new_min))

    def pop(self) -> None:
        if self.stack:
            self.stack.pop()

    def top(self) -> int:
        if self.stack:
            return self.stack[-1][0]
        return None
    def getMin(self) -> int:
        if self.stack:
            return self.stack[-1][1]
        return None


